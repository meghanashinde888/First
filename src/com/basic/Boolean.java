package com.basic;

public class Boolean 
{
	public static void main(String args[])
	{
		int p=100,q=10;
		System.out.println("p==q "+(p==q));
		System.out.println("p!=q "+(p!=q));
		System.out.println("p>q "+(p>q));
		System.out.println("p<q "+(p<q));
		System.out.println("p>=q "+(p>=q));
		System.out.println("p<=q "+(p<=q));

		
	}

}
